package Nivel_1;

public class _05_Constantes {

    public static void main(String[] args) {
        
        /* Una constante es un espacio en la memoria, donde 
        se almacena un valor que no va a cambiar en todo el programa */

        final int numero = 10; // [final] es como se declara una constante
        //numero = 15;

        System.out.println(numero);
        
    }
    
}